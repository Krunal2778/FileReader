import express from 'express';
import { auth, optionalAuth } from '../middlewares/auth';
import { validateBody, validateParams, validateQuery } from '../middlewares/validation';
import { createPostSchema } from '@shared/validation';
import { 
  getPosts, 
  getPostById, 
  createPost, 
  updatePost, 
  deletePost,
  getComments,
  addComment,
  deleteComment,
  likePost,
  unlikePost,
  savePost,
  unsavePost,
  followPost,
  unfollowPost,
  getSavedPosts,
  getFollowedPosts,
  getUserPosts
} from '../controllers/posts';
import { z } from 'zod';

const router = express.Router();

// Validation schemas
const postIdParam = z.object({
  id: z.string().uuid()
});

const commentIdParam = z.object({
  commentId: z.string().transform(val => parseInt(val, 10))
});

const postQueryParams = z.object({
  page: z.string().optional().transform(val => val ? parseInt(val, 10) : 1),
  limit: z.string().optional().transform(val => val ? parseInt(val, 10) : 10),
  category: z.string().optional(),
  location: z.string().optional(),
  search: z.string().optional()
});

const commentSchema = z.object({
  content: z.string().min(1, 'Comment cannot be empty').max(500, 'Comment is too long')
});

/**
 * @swagger
 * /posts:
 *   get:
 *     summary: Get all posts with filtering
 *     tags: [Posts]
 *     parameters:
 *       - in: query
 *         name: page
 *         schema:
 *           type: integer
 *         description: Page number
 *       - in: query
 *         name: limit
 *         schema:
 *           type: integer
 *         description: Items per page
 *       - in: query
 *         name: category
 *         schema:
 *           type: string
 *         description: Filter by category
 *       - in: query
 *         name: location
 *         schema:
 *           type: string
 *         description: Filter by location
 *       - in: query
 *         name: search
 *         schema:
 *           type: string
 *         description: Search query
 *     responses:
 *       200:
 *         description: A paginated list of posts
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/PaginatedResponse'
 */
router.get('/', optionalAuth, validateQuery(postQueryParams), getPosts);

/**
 * @swagger
 * /posts/{id}:
 *   get:
 *     summary: Get a post by ID
 *     tags: [Posts]
 *     parameters:
 *       - in: path
 *         name: id
 *         schema:
 *           type: string
 *           format: uuid
 *         required: true
 *         description: Post UUID
 *     responses:
 *       200:
 *         description: Post details
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: success
 *                 data:
 *                   $ref: '#/components/schemas/PostWithDetails'
 *       404:
 *         description: Post not found
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.get('/:id', optionalAuth, validateParams(postIdParam), getPostById);

/**
 * @swagger
 * /posts:
 *   post:
 *     summary: Create a new post
 *     tags: [Posts]
 *     security:
 *       - BearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - title
 *               - description
 *               - categoryId
 *               - location
 *               - locationDetails
 *             properties:
 *               title:
 *                 type: string
 *               description:
 *                 type: string
 *               categoryId:
 *                 type: integer
 *               subcategoryId:
 *                 type: integer
 *               location:
 *                 type: string
 *                 enum: [amritsar, jalandhar, ludhiana, chandigarh, gurugram]
 *               locationDetails:
 *                 type: string
 *               imageUrl:
 *                 type: string
 *               visibility:
 *                 type: string
 *                 enum: [public, private]
 *                 default: public
 *               metadata:
 *                 type: object
 *                 additionalProperties:
 *                   type: string
 *     responses:
 *       201:
 *         description: Post created successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: success
 *                 data:
 *                   $ref: '#/components/schemas/PostWithDetails'
 *       400:
 *         description: Validation error
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       401:
 *         description: Unauthorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.post('/', auth, validateBody(createPostSchema), createPost);

